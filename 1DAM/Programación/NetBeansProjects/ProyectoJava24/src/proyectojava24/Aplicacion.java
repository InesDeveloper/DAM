    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        System.out.println("Voy a insertar un registro");
        
        boolean pasas = true;
        
        // Valido el correo electronico
        String correo = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$";
        Pattern patron = Pattern.compile(correo, Pattern.CASE_INSENSITIVE);
        Matcher frase = patron.matcher(jTextField4.getText());
        boolean encontrado = frase.find();
        if(encontrado) { pasas = true; } else { pasas = false; }
        
        // Busco delete o truncate en el nombre
        // String nombre = "\\Bdelete|truncate\\B";
        // Pattern patron2 = Pattern.compile(nombre, Pattern.CASE_INSENSITIVE);
        // Matcher frase2 = patron2.matcher(jTextField1.getText());
        // boolean encontrado2 = frase2.find();
        // if(encontrado2) { pasas = false; } else { pasas = true; }
        
        if(pasas) {
            try{                                                                // Primero intenta hacer algo
                FileWriter miarchivo = new FileWriter("agenda.txt", true);      // Abre un archivo y pones true para a√±adir lineas en lugar de sustituir el contenido
                miarchivo.write(jTextField1.getText()+"|"+jTextField2.getText()+"|"+jTextField3.getText()+"|"+jTextField4.getText()+"\n");// Le escribo algo de contenido
                miarchivo.close();                                              // Cierra los recursos depues de usarlos
            }catch(IOException e){                                              // En el caso de que el try felle
                e.printStackTrace();
            }
            jTextField1.setText("");jTextField2.setText("");jTextField3.setText("");jTextField4.setText("");
            jLabel6.setText("OK, tu registro se ha guardado correctamente");
        } else {
            System.out.println("No es valido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

