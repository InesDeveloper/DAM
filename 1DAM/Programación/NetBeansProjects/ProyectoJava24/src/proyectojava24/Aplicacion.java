    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //jTextArea1.setText("Esto es un texto que vas a recibir");
        String longaniza = "";
        try{                                                                    // Primero intento hacer algo
            File miotroarchivo = new File("agenda.txt");                        // Abro un archivo
            Scanner lector = new Scanner(miotroarchivo);                        // Leo el contenido del archivo
            while(lector.hasNextLine()){                                        // Mientras que el archivo tenga lineas de texto
                longaniza += lector.nextLine() + "\n";                          // Imprime la linea actual n la pantalla
            }
        }catch(IOException e){                                                  // En el caso de que de error de lectura
            e.printStackTrace();                                                // Dime en qu√© ha consistido el error
        }
        
        longaniza = longaniza.replace("|", " ");
        jTextArea1.setText(longaniza);
    }//GEN-LAST:event_jButton2ActionPerformed

