// Operador igualdad
double operando1 = 4;
double operando2 = 3;
bool resultado;
resultado = operando1 == operando2;
Console.WriteLine("Es cierdo que el operando1, ("+operando1+") es igual que el operando 2, ("+operando2+")?:"+resultado);
resultado = operando1 != operando2;
Console.WriteLine("Es cierdo que el operando1, (" + operando1 + ") no es igual que el operando 2, (" + operando2 + ")?:" + resultado);
resultado = operando1 < operando2;
Console.WriteLine("Es cierdo que el operando1, (" + operando1 + ") es menor que el operando 2, (" + operando2 + ")?:" + resultado);
resultado = operando1 > operando2;
Console.WriteLine("Es cierdo que el operando1, (" + operando1 + ") es mayor que el operando 2, (" + operando2 + ")?:" + resultado);
resultado = operando1 >= operando2;
Console.WriteLine("Es cierdo que el operando1, (" + operando1 + ") es mayor o igual que el operando 2, (" + operando2 + ")?:" + resultado);
resultado = operando1 <= operando2;
Console.WriteLine("Es cierdo que el operando1, (" + operando1 + ") es menor o igual que el operando 2, (" + operando2 + ")?:" + resultado);